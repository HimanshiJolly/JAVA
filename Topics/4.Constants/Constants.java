public class Constants
{
    public static void main(String[] args){
        //constants
        int age=18;
        System.out.println("Age: "+age);
        age=19;
        System.out.println("Age: "+age);
        final float pi = 3.14F;
        System.out.println("pi: "+pi);
        age=14;
        System.out.println("pi: "+pi);

    }
}